# Аялалын бүх алхмын богино нэр (явцын зураасанд харагдана).
JOURNEY = [
    "Хийд", "Шувуу", "Зээр", "Хулгана", "Худалдаа", "Ордон",
    "Шөнө-1", "Шөнө-2", "Шөнө-3", "Бөгж", "Маргаан", "Царцаа",
]


def progress_bar(stage):
    """Аялал хаана хүрсэнг харуулах хэвтээ зураас."""
    cells = []
    for i in range(len(JOURNEY)):
        if i < stage:
            cells.append(color("●", "green"))
        elif i == stage:
            cells.append(color("◆", "yellow", bold=True))
        else:
            cells.append(color("○", "grey"))
    return " ".join(cells)


def header(title):
    """Дэлгэцийн дээд хэсэг: гарчиг, явцын зураас, сурсан тарнинууд."""
    clear()
    box([color(title, "yellow", bold=True)], c="magenta", center=True)
    print(" " + progress_bar(player["stage"]) +
          color(f"   ({player['stage'] + 1}/{len(JOURNEY)})", "grey"))
    if player["learned"]:
        print(color(" Ажигласан тарни:", "cyan"))
        for ph in player["learned"]:
            print(color("   • " + ph, "cyan"))
    line()


def learn(phrase):
    """Замдаа ажигласан тарнийг тогтоож, мэдэгдэнэ."""
    if phrase not in player["learned"]:
        player["learned"].append(phrase)
    slow("\n  📖 Чи тогтоож авлаа:  «" + phrase + "»", "green", bold=True)


