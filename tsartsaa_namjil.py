def death(reason):
    """Үхлийн дэлгэц харуулж, дахин оролдох эсэхийг асууна.
    True буцаавал тоглогч дахин оролдоно, False бол тоглоомоос гарна."""
    print()
    for ln in SKULL:
        print(color(ln, "red"))
    box([color("Ү Х Э Л", "red", bold=True), "", reason], c="red", center=True)
    again = ask("\n  Дахин оролдох уу? (тийм / үгүй): ")
    return again.startswith("т")


