COLORS = {
    "red": "31", "green": "32", "yellow": "33", "blue": "34",
    "magenta": "35", "cyan": "36", "white": "37", "grey": "90",
}


def color(text, c, bold=False):
    """Тэмдэгт мөрийг өнгөтэй болгож буцаана (өнгө унтраалттай бол хэвээр)."""
    if not USE_COLOR:
        return text
    code = COLORS.get(c, "37")
    start = "1;" if bold else ""
    return f"\033[{start}{code}m{text}\033[0m"


def clear():
    """Дэлгэцийг цэвэрлэнэ."""
    if USE_COLOR:
        print("\033[2J\033[H", end="")


def visible_len(s):
    """Өнгөний нууц кодыг тооцохгүйгээр харагдах урт (re ашиглахгүй)."""
    count = 0
    i = 0
    while i < len(s):
        if s[i] == "\033":
            while i < len(s) and s[i] != "m":
                i += 1
            i += 1
        else:
            count += 1
            i += 1
    return count


def pad(s, width, center=False):
    """Тэмдэгт мөрийг харагдах уртаар нь width хүртэл хоосон зайгаар дүүргэнэ."""
    extra = width - visible_len(s)
    if extra < 0:
        extra = 0
    if center:
        left = extra // 2
        return " " * left + s + " " * (extra - left)
    return s + " " * extra


def box(lines, width=64, c="cyan", center=False):
    """Текстийн мөрүүдийг гоё хүрээгээр хүрээлж хэвлэнэ."""
    inner = width - 4
    print(color("╔" + "═" * (width - 2) + "╗", c))
    for ln in lines:
        print(color("║", c) + " " + pad(ln, inner, center) + " " + color("║", c))
    print(color("╚" + "═" * (width - 2) + "╝", c))


def line():
    print(color("─" * 64, "grey"))


def slow(text="", c=None, bold=False, delay=0.012):
    """Текстийг бичгийн машин шиг аажуухан хэвлэнэ (өнгөтэй байж болно)."""
    if c is not None:
        text = color(text, c, bold)
    if not SLOW_TEXT:
        print(text)
        return
    for ch in text:
        print(ch, end="", flush=True)
        # Өнгөний нууц код дотор зогсолт хийхгүй.
        if ch not in "\033[0123456789;m":
            time.sleep(delay)
    print()


def pause(msg="( ҮРГЭЛЖЛҮҮЛЭХ бол Enter дар )"):
    input("\n" + color(msg, "grey"))


def ask(prompt):
    """Тоглогчоос мөр асууж, цэвэрлэж, жижиг үсгээр буцаана."""
    return input(color(prompt, "yellow", bold=True)).strip().lower()


