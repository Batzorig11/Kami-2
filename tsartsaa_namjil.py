def choose(options):
    """Дугаарласан сонголтуудыг харуулж, зөв дугаар авах хүртэл асууна.
    options -- (текст, ok, үр_дагавар, [сурах_тарни]) хэлбэрийн дөрвөлүүдийн жагсаалт.
    Сонгосон дөрвөлийг буцаана."""
    print()
    for i in range(len(options)):
        print(color(f"  [{i + 1}] ", "white", bold=True) + options[i][0])
    while True:
        raw = ask("\n  Чиний сонголт (дугаар): ")
        if raw.isdigit():
            n = int(raw)
            if 1 <= n <= len(options):
                return options[n - 1]
        print(color("  1-ээс " + str(len(options)) + " хүртэлх дугаар бичнэ үү.", "red"))


