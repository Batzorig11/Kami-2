def play(stage, title, story_lines, question, options):
    """Нэг ердийн хэсгийг тоглуулах нийтлэг функц.
    options доторх зөв сонголт нэгээс олонгүй гэж үзнэ."""
    player["stage"] = stage
    while True:
        header(title)
        for ln in story_lines:
            slow(ln)
        slow("\n" + question, "white", bold=True)
        text, ok, outcome, extra = choose(options)
        print()
        slow(outcome, "green" if ok else "red")
        if ok:
            if extra:
                learn(extra)
            pause()
            return True
        if not death("Чиний аялал энд тасарлаа."):
            return False
        # death() True буцаасан тул while давталт хэсгийг дахин тоглуулна.


