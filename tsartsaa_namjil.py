# Хэрэв терминал муухай тэмдэгт харуулбал эдгээрийг False болго.
USE_COLOR = True
SLOW_TEXT = True          # текстийг бичгийн машин шиг аажуухан хэвлэх
os.system("")             # Windows 10+ дээр ANSI өнгийг асаадаг жижиг заль

