def highlight_python(code):
    """Маш энгийн Python өнгөлөгч — HTML span гаргана."""
    # Тэмдэгт мөрийг token болгон задлах regex
    token_re = re.compile(
        r'(?P<comment>#[^\n]*)'
        r'|(?P<string>"""(?:.|\n)*?"""|"[^"\n]*"|\'[^\'\n]*\')'
        r'|(?P<number>\b\d+\b)'
        r'|(?P<name>[A-Za-z_Ѐ-ӿ][A-Za-z0-9_Ѐ-ӿ]*)'
        r'|(?P<other>[\s\S])',
        re.UNICODE,
    )
    out = []
    for m in token_re.finditer(code):
        kind = m.lastgroup
        text = m.group()
        esc = html.escape(text)
        if kind == "comment":
            out.append(f'<span class="c-com">{esc}</span>')
        elif kind == "string":
            out.append(f'<span class="c-str">{esc}</span>')
        elif kind == "number":
            out.append(f'<span class="c-num">{esc}</span>')
        elif kind == "name":
            if text in KEYWORDS:
                out.append(f'<span class="c-kw">{esc}</span>')
            elif text in BUILTINS:
                out.append(f'<span class="c-fn">{esc}</span>')
            else:
                out.append(esc)
        else:
            out.append(esc)
    return "".join(out)


