# ----------------------------------------------------------------------
# ТОГЛОГЧИЙН ТӨЛӨВ
# ----------------------------------------------------------------------

player = {
    "name": "Царцаа Намжил",
    "learned": [],     # замдаа ажиглаж тогтоосон тарниуд
    "stage": 1,        # хэддэх хэсэгт явааг
}


# ----------------------------------------------------------------------
# ТУСЛАХ ФУНКЦУУД (сурагчдын бичих)
# ----------------------------------------------------------------------

def header(title):
    """Дэлгэцийн дээд хэсэг: гарчиг, явц, сурсан тарниудыг харуулна."""
    clear()
    box([title], "magenta", center=True)
    print(color("  Аялал: " + str(player["stage"]) + " / 12", "grey"))
    if len(player["learned"]) > 0:
        print(color("  Ажигласан тарни:", "cyan"))
        for tarni in player["learned"]:
            print(color("    • " + tarni, "cyan"))
    print()


def ask_number(count):
    """1-ээс count хүртэлх ЗӨВ дугаар авах хүртэл дахин дахин асууна."""
    while True:
        answer = ask("  Чиний сонголт (дугаар): ")
        if answer.isdigit():
            number = int(answer)
            if 1 <= number <= count:
                return number
        print(color("  1-ээс " + str(count) + " хүртэлх дугаар бичнэ үү.", "red"))


def died():
    """Үхлийн дэлгэц. Дахин оролдвол True, гарвал False буцаана."""
    print()
    box(["Ү Х Э Л", "", "Чиний аялал энд тасарлаа."], "red", center=True)
    again = ask("  Дахин оролдох уу? (тийм / үгүй): ")
    return again.startswith("т")


def play_scene(scene):
    """Нэг хэсгийг тоглуулна. Зөв сонгож гарвал True, тоглоомоос гарвал False.
    scene — гарчиг, өгүүлэмж, асуулт, сонголтуудтай толь бичиг (dict)."""
    while True:
        header(scene["title"])
        for story_line in scene["story"]:
            slow(story_line)
        print()
        slow(scene["question"], "yellow", bold=True)
        print()

        # Сонголтуудыг дугаарлан хэвлэнэ.
        choices = scene["choices"]
        for i in range(len(choices)):
            print(color("  [" + str(i + 1) + "] ", "white", bold=True) + choices[i]["text"])

        pick = ask_number(len(choices))
        chosen = choices[pick - 1]
        print()

        if chosen["good"]:
            slow(chosen["result"], "green")
            if chosen["learn"] != "":
                player["learned"].append(chosen["learn"])
                slow("  📖 Чи тогтоож авлаа: «" + chosen["learn"] + "»", "green", bold=True)
            pause()
            return True
        else:
            slow(chosen["result"], "red")
            if died():
                continue          # дахин энэ хэсгийг тоглоно
            else:
                return False      # тоглогч гарлаа


